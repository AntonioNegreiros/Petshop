public class Tutor {
        int IdTutor;
        String nome;
        String telefone;
        String email;
    
    public Tutor(
    int IdTutor,
    String nome,
    String telefone,
    String email
    ){
        this.IdTutor = IdTutor;
        this.nome = nome;
        this.telefone = telefone; 
        this.email = email;
    }

}
